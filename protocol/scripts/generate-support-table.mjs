#!/usr/bin/env node
// Regenerates the README "Supported Unity versions" table (between the SUPPORT-TABLE markers) from the
// floor-matrix.yml CI matrix — so the support claims are derived from what CI actually tests, not hand-written
// (roadmap A2). Run: node protocol/scripts/generate-support-table.mjs
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, '..', '..');
const workflow = readFileSync(join(root, '.github', 'workflows', 'floor-matrix.yml'), 'utf8');
const readmePath = join(root, 'README.md');

// Pull the matrix unity versions: list items under `unity:` (e.g. "- 2020.3.49f1"); skip commented "# - ...".
const versions = [];
let inUnity = false;
for (const line of workflow.split(/\r?\n/)) {
  if (/^\s*unity:\s*$/.test(line)) { inUnity = true; continue; }
  if (!inUnity) continue;
  const m = line.match(/^\s*-\s*(\d+\.\d+\.\d+\S*)\s*$/);
  if (m) { versions.push(m[1]); continue; }
  const t = line.trim();
  if (t && !t.startsWith('#') && !t.startsWith('-')) break; // a less-indented key ends the matrix block
}
if (versions.length === 0) { console.error('No matrix versions found in floor-matrix.yml'); process.exit(1); }

const lts = (v) => v.replace(/^(\d+\.\d+).*/, '$1');
const rows = versions.map((v) => `| ${lts(v)} LTS | \`${v}\` | ✅ compiles + EditMode tests green |`).join('\n');

const table = [
  '<!-- SUPPORT-TABLE:START -->',
  '<!-- Generated by protocol/scripts/generate-support-table.mjs from the floor-matrix CI — do not edit. -->',
  '',
  '| Unity LTS | CI patch | Editor C# + EditMode |',
  '|---|---|---|',
  rows,
  '',
  '**Floor = 2020.3 LTS.** The versions above run the full EditMode suite in the',
  '[floor-matrix CI](.github/workflows/floor-matrix.yml) on every release tag. **2019.4** is a guarded best-effort',
  'target — both branches of each version-divergent API are kept under `#if` (see [`COMPATIBILITY.md`](COMPATIBILITY.md)) —',
  'but is not in the CI matrix. **Unity 6 (6000.x)** is guarded; a CI host project is pending.',
  '<!-- SUPPORT-TABLE:END -->',
].join('\n');

let readme = readFileSync(readmePath, 'utf8');
const re = /<!-- SUPPORT-TABLE:START -->[\s\S]*?<!-- SUPPORT-TABLE:END -->/;
if (!re.test(readme)) { console.error('SUPPORT-TABLE markers not found in README.md'); process.exit(1); }
writeFileSync(readmePath, readme.replace(re, table));
console.log(`Updated README support table from ${versions.length} CI matrix versions: ${versions.join(', ')}`);
